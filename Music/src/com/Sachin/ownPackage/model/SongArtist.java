package com.Sachin.ownPackage.model;

public class SongArtist {
    private String onArtistName;
    private String albumName;
    private int track;

    public String getOnArtistName() {
        return onArtistName;
    }

    public void setOnArtistName(String onArtistName) {
        this.onArtistName = onArtistName;
    }

    public String getAlbumName() {
        return albumName;
    }

    public void setAlbumName(String albumName) {
        this.albumName = albumName;
    }

    public int getTrack() {
        return track;
    }

    public void setTrack(int track) {
        this.track = track;
    }
}
